// plugin-compiler/src/shims/stop-shim-template.js
'use strict';

/**
 * Returns the source code for the generated stop-shim.js.
 * @param {Object} options
 * @param {boolean} options.editTracking
 * @param {boolean} options.sessionStats
 * @param {boolean} options.subagentGuard
 * @param {string} options.pluginRoot — resolved plugin root path expression
 * @returns {string}
 */
function generateStopShim({ editTracking, sessionStats, subagentGuard, subagentStart, pluginRoot }) {
  const sections = [];

  if (editTracking) {
    sections.push(`
    // --- Edit Tracking (shimmed from PostToolUse(Edit|Write)) ---
    const editEntries = toolCalls.filter(tc =>
      ['write', 'edit', 'Write', 'Edit'].includes(tc.tool_name || tc.function?.name || '')
    );
    if (editEntries.length > 0) {
      try {
        const trackEdits = require(path.join(PLUGIN_ROOT, 'hooks', 'track-edits.js'));
        if (typeof trackEdits.trackFromTranscript === 'function') {
          trackEdits.trackFromTranscript(editEntries, cwd);
        }
      } catch (e) {
        console.error('[stop-shim] edit tracking error:', e.message);
      }
    }`);
  }

  if (sessionStats) {
    sections.push(`
    // --- Session Stats (shimmed from PostToolUse(Skill)) ---
    const skillEntries = toolCalls.filter(tc =>
      ['Skill', 'skill'].includes(tc.tool_name || tc.function?.name || '')
    );
    if (skillEntries.length > 0) {
      try {
        const trackStats = require(path.join(PLUGIN_ROOT, 'hooks', 'track-session-stats.js'));
        if (typeof trackStats.trackFromTranscript === 'function') {
          trackStats.trackFromTranscript(skillEntries, cwd);
        }
      } catch (e) {
        console.error('[stop-shim] session stats error:', e.message);
      }
    }`);
  }

  if (subagentGuard) {
    sections.push(`
    // --- Subagent Guard (shimmed from SubagentStop) ---
    const agentEntries = toolCalls.filter(tc =>
      ['Agent', 'agent', 'dispatch_agent'].includes(tc.tool_name || tc.function?.name || '')
    );
    if (agentEntries.length > 0) {
      try {
        const subagentGuard = require(path.join(PLUGIN_ROOT, 'hooks', 'subagent-guard.js'));
        if (typeof subagentGuard.checkFromTranscript === 'function') {
          subagentGuard.checkFromTranscript(agentEntries, cwd);
        }
      } catch (e) {
        console.error('[stop-shim] subagent guard error:', e.message);
      }
    }`);
  }

  if (subagentStart) {
    sections.push(`
    // --- Subagent Start (shimmed from SubagentStart) ---
    const agentStartEntries = toolCalls.filter(tc =>
      ['Agent', 'agent', 'dispatch_agent'].includes(tc.tool_name || tc.function?.name || '')
    );
    if (agentStartEntries.length > 0) {
      try {
        const subagentStartHook = require(path.join(PLUGIN_ROOT, 'hooks', 'subagent-start.js'));
        if (typeof subagentStartHook.handleFromTranscript === 'function') {
          subagentStartHook.handleFromTranscript(agentStartEntries, cwd);
        }
      } catch (e) {
        console.error('[stop-shim] subagent start error:', e.message);
      }
    }`);
  }

  return `#!/usr/bin/env node
// GENERATED by hookbridge — do not edit by hand.
'use strict';

const fs = require('fs');
const path = require('path');

const PLUGIN_ROOT = ${pluginRoot};

async function main() {
  let input = '';
  for await (const chunk of process.stdin) input += chunk;

  let data;
  try {
    data = JSON.parse(input);
  } catch {
    process.stdout.write('{}');
    return;
  }

  const cwd = data.cwd || process.cwd();
  const transcriptPath = data.transcript_path;

  if (!transcriptPath) {
    console.error('[stop-shim] No transcript_path provided — shim cannot run.');
    process.stdout.write('{}');
    return;
  }

  let transcript;
  try {
    transcript = JSON.parse(fs.readFileSync(transcriptPath, 'utf8'));
  } catch (e) {
    console.error('[stop-shim] Failed to read/parse transcript:', e.message);
    process.stdout.write('{}');
    return;
  }

  const toolCalls = [];
  for (const entry of (transcript || [])) {
    if (entry.type === 'tool_call' || entry.type === 'function_call') {
      toolCalls.push(entry);
    }
    if (Array.isArray(entry.content)) {
      for (const block of entry.content) {
        if (block.type === 'tool_use') {
          toolCalls.push({ tool_name: block.name, tool_input: block.input });
        }
      }
    }
  }
${sections.join('\n')}

  process.stdout.write('{}');
}

main().catch(() => process.stdout.write('{}'));
`;
}

module.exports = { generateStopShim };
